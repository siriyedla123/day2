str=input()
a=0
j=0
e=0
o=0
u=0
for i in str:
    if (i=='a' or i=='A'):
        a=a+1
    elif (i=='e' or i=='E'):
        e=e+1
    elif (i=='i' or i=='I'):
        j=j+1
    elif (i=='o' or i=='O'):
        o=o+1
    elif (i=='u' or i=='U'):
        u=u+1
print(a)
print(e)
print(j)
print(o)
print(u)
    
    

